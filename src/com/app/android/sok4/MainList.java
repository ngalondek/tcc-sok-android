    private void goto_song_index(long index) {
	// Switch to report page
	Intent intent = new Intent();

	intent.setClass(MainList.this, SongBoard.class);
	Bundle bundle = new Bundle();
	int start = song.indexOf(songTitles[(int) index]);
	int end = song.indexOf('【', start + 1) - 1;

	// Toast.makeText(Bmi.this, "end="+new
	// Long(end).toString(),Toast.LENGTH_SHORT).show();
	if (end > song.length() || (end < 0)) {
	    end = song.length();
	}

	bundle.putString("SONG_TITLE", songTitles[(int) index]);
	bundle.putString("SONG_TEXT", song.substring(start, end));
	intent.putExtras(bundle);
	startActivity(intent);

    }

